def is_proxy_working(proxy):
    url = 'https://www.google.com'
    try:
        response = requests.get(url, proxies=proxy, timeout=5)
        if response.status_code == 200:
            return True
    except requests.exceptions.RequestException:
        return False

def send_support_request(email, phone, message, proxies=None):
    url = 'https://telegram.org/support'
    data = {
        'email': email,
        'phone': phone,
        'message': message,
    }

    try:
        response = requests.post(url, data=data, proxies=proxies)
        if response.status_code == 200:
            print(colored(f'Запрос с {email} и номера {phone} успешно отправлен.', 'blue'))
        else:
            print(f'Ошибка при отправке запроса с {email}:', response.status_code)
    except requests.exceptions.RequestException as e:
        print(f'Ошибка при подключении через прокси {proxies}: {e}')
        return False
    return True

